public class ChangeChar {
	




	public static void main(String[] args){
		//演示转义字符的使用
		//“\t”的使用
		System.out.println("北京\t上海\t深圳");
		//“\n”的使用
		System.out.println("tom\njerry\nrose");
		//“\\”的使用
		System.out.println("c:\\windows\\System32");
		System.out.println("书名\t作者\t价格\n西游记\t吴承恩\t120");
	}
}